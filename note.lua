#!/usr/bin/env lua

-- ==========================================
-- Настройка параметров по умолчанию
-- ==========================================
local name = nil
local dir  = "."

-- ==========================================
-- Парсинг аргументов командной строки
-- ==========================================
for i = 1, #arg do
    if arg[i] == "--name" and arg[i+1] then
        name = arg[i+1]
    elseif arg[i] == "--dir" and arg[i+1] then
        dir = arg[i+1]
    end
end

-- Проверка обязательного параметра
if not name then
    print("Ошибка: параметр --name обязателен.")
    print("Использование: lua create_note.lua --name \"Название заметки\" [--dir ./путь/к/папке]")
    os.exit(1)
end

-- ==========================================
-- Подготовка пути и имени файла
-- ==========================================
-- Заменяем пробелы на нижние подчеркивания (строго по ТЗ)
-- Добавляем расширение .md (стандарт для подобных заметок).
-- Если нужно другое расширение, измените строку ниже.
local filename = name:gsub(" ", "_") .. ".md"

-- Нормализуем путь: добавляем разделитель, если его нет
if dir:sub(-1) ~= "/" and dir:sub(-1) ~= "\\" then
    dir = dir .. "/"
end

local filepath = dir .. filename

-- ==========================================
-- Формирование содержимого
-- ==========================================
-- Текущая дата в формате YYYY-MM-DD (ISO 8601)
local current_date = os.date("%Y-%m-%d")

-- Шаблон заметки (в точности по вашему образцу)
local template = [[{{shorttitle}}
===

tags:

---


---
links:

backlinks:

date: {{date}}]]

-- Подстановка значений
local content = template:gsub("{{shorttitle}}", name)
content = content:gsub("{{date}}", current_date)

-- ==========================================
-- Запись в файл
-- ==========================================
local file, err = io.open(filepath, "w")
if not file then
    print("Ошибка при создании файла '" .. filepath .. "': " .. err)
    os.exit(1)
end

-- Добавляем завершающий перевод строки для корректного отображения в редакторах
file:write(content .. "\n")
file:close()

print("✅ Заметка успешно создана: " .. filepath)
